import numpy as np  # 숫자 배열을 다루기 쉽게 해주는 라이브러리입니다.
from sklearn.metrics import precision_score, recall_score, f1_score  # 분류 성능지표를 계산하는 함수들을 불러옵니다.


# [1단계] 실험할 데이터 만들기
y_pred = np.array([0, 1, 1, 0, 1, 1, 1, 0])  # 모델이 예측한 결과입니다. 1은 양성, 0은 음성으로 생각하면 됩니다.
y_true = np.array([0, 1, 0, 0, 0, 0, 1, 1])  # 실제 정답입니다. 모델 예측과 비교할 기준이 됩니다.


# ------------------------------------------------------------
# [수동 계산을 위한 중간 점검]
# 데이터 8개를 하나씩 비교해보면 다음과 같습니다.
# TP (실제 1, 예측 1) = 2개
# FP (실제 0, 예측 1) = 3개 (오경보)
# FN (실제 1, 예측 0) = 1개 (놓침)
# TN (실제 0, 예측 0) = 2개
# ------------------------------------------------------------


# [3단계] 정밀도(Precision) 계산
# 공식: TP / (TP + FP) = 2 / (2 + 3) = 0.4
precision = precision_score(y_true, y_pred)  # 모델이 1이라고 예측한 것들 중에서 실제로 맞은 비율을 계산합니다.
print("1. 정밀도(Precision)")  # 첫 번째 결과 제목을 출력합니다.
print(f"결과: {precision}")  # 계산된 정밀도 값을 출력합니다.
print()  # 보기 좋게 한 줄 띄웁니다.


# [4단계] 민감도/재현율(Recall) 계산
# 공식: TP / (TP + FN) = 2 / (2 + 1) = 0.666...
recall = recall_score(y_true, y_pred)  # 실제 1인 데이터들 중에서 모델이 얼마나 잘 찾아냈는지 계산합니다.
print("2. 민감도(Recall)")  # 두 번째 결과 제목을 출력합니다.
print(f"결과: {recall}")  # 계산된 민감도 값을 출력합니다.
print()  # 보기 좋게 한 줄 띄웁니다.


# [5단계] F1 스코어(F1-score) 계산
# 정밀도와 민감도의 조화평균. 둘 중 하나라도 너무 낮으면 F1 점수도 깎입니다.
f1 = f1_score(y_true, y_pred)  # 정밀도와 민감도를 함께 고려한 종합 점수를 계산합니다.
print("3. F1 스코어(F1-score)")  # 세 번째 결과 제목을 출력합니다.
print(f"결과: {f1}")  # 계산된 F1 점수를 출력합니다.
