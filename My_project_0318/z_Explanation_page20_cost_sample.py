import numpy as np  # 숫자 배열과 수학 계산을 쉽게 처리하기 위한 라이브러리입니다.


# [1단계] 시그모이드 함수
def sigmoid(z):  # z라는 점수를 받아 0과 1 사이 값으로 바꾸는 함수를 정의합니다.
    return 1 / (1 + np.exp(-z))  # 시그모이드 공식입니다. 점수를 확률처럼 해석할 수 있게 바꿔줍니다.


# [2단계] 가설 함수 (Hypothesis Function)
def hypothesis_function(x, theta):  # 입력 데이터 x와 가중치 theta를 이용해 예측 확률을 구하는 함수입니다.
    z = np.dot(x, theta)  # 입력값과 가중치를 곱해서 더한 종합 점수 z를 계산합니다.
    return sigmoid(z)  # 종합 점수 z를 시그모이드 함수에 넣어 최종 확률로 바꿉니다.


# [3단계] 비용 함수 (Cost Function / Log Loss)
def compute_cost(x, y, theta):  # 모델의 예측이 실제 정답과 얼마나 다른지 숫자로 계산하는 함수입니다.
    m = y.shape[0]  # 전체 데이터 개수를 구합니다. 평균 비용을 계산할 때 필요합니다.
    h = hypothesis_function(x, theta)  # 현재 가중치 theta로 계산한 모델의 예측 확률값들입니다.

    # 로그 손실 공식 적용
    term1 = y.T.dot(np.log(h))  # 실제 정답이 1인 경우에 대한 손실 계산 부분입니다.
    term2 = (1 - y).T.dot(np.log(1 - h))  # 실제 정답이 0인 경우에 대한 손실 계산 부분입니다.

    j = (-1.0 / m) * (term1 + term2)  # 전체 손실을 평균내고 부호를 바꿔 최종 비용값 j를 만듭니다.
    return j  # 계산된 비용값을 함수 밖으로 돌려줍니다.


# [4단계] 실제 데이터 대입 및 결과 출력
x = np.array([  # 입력 데이터 x를 2차원 배열로 만듭니다.
    [1, 2],  # 첫 번째 데이터: 특징값 2개
    [3, 4],  # 두 번째 데이터: 특징값 2개
    [5, 6],  # 세 번째 데이터: 특징값 2개
])

# 실제 정답 (1: 고장, 0: 정상, 1: 고장)
y = np.array([1, 0, 1])  # 각 데이터에 대한 실제 정답입니다.

# 임의로 설정한 가중치
theta = np.array([0.1, 0.2])  # 모델이 현재 사용하고 있다고 가정한 가중치입니다.

# 1. 먼저 예측 확률 확인하기
predictions = hypothesis_function(x, theta)  # 각 입력 데이터에 대해 모델이 예측한 확률을 계산합니다.
print("=== 1. 모델의 예측 확률 ===")  # 첫 번째 출력 구역 제목입니다.
print(predictions)  # 예측된 확률들을 그대로 출력합니다.
print()  # 줄을 한 줄 띄워 다음 출력과 구분합니다.

# 2. 이 예측이 얼마나 틀렸는지 비용 계산하기
cost = compute_cost(x, y, theta)  # 예측 결과와 실제 정답을 비교하여 비용값을 계산합니다.
print("=== 2. 현재 모델의 비용(Cost) ===")  # 두 번째 출력 구역 제목입니다.
print(f"변형 점수: {cost:.4f}")  # 비용값을 소수점 4자리까지 출력합니다.
